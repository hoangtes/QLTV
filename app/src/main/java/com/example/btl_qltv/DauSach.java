package com.example.btl_qltv;

public class DauSach {
    String anh;
    String tenSach;
    Double danhGia;
    String tacGia;
    String theLoai;
    int soLuong;

    public DauSach(String anh, String tenSach, Double danhGia, String tacGia, String theLoai, int soLuong) {
        this.anh = anh;
        this.tenSach = tenSach;
        this.danhGia = danhGia;
        this.tacGia = tacGia;
        this.theLoai = theLoai;
        this.soLuong = soLuong;
    }
}
